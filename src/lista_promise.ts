// Lista de Exercícios – Prática com Promises
// Exercício 1

// Crie um programa que simule uma tarefa demorada.
// A tarefa deve esperar 2 segundos antes de terminar e, ao final, retornar a mensagem:

// "Tarefa finalizada"

// Depois disso, mostre a mensagem no console.

// Exercício 2

// Crie uma tarefa assíncrona que simule o resultado de jogar uma moeda.

// Regras:

// Existe 50% de chance de sucesso e 50% de chance de erro.

// Se for sucesso, retorne a mensagem "Cara".

// Se for erro, retorne "Coroa".

// O programa principal deve tratar corretamente o sucesso e o erro e mostrar o resultado no console.

// Exercício 3

// Simule um sistema simples de login.

// Regras:

// O sistema recebe um usuário e uma senha.

// Se o usuário for "admin" e a senha "1234", a operação deve ser considerada bem-sucedida e retornar a mensagem:

// "Login realizado com sucesso"

// Caso contrário, deve retornar um erro com a mensagem:

// "Usuário ou senha incorretos"

// O programa principal deve mostrar a resposta no console.

// Exercício 4

// Crie uma tarefa assíncrona que espere 3 segundos e depois gere um número aleatório entre 1 e 10.

// Depois que o número for gerado, ele deve ser mostrado no console.

// Exercício 5

// Simule um processo em duas etapas:

// Primeiro, uma tarefa deve gerar um número aleatório entre 1 e 5.

// Depois, outra tarefa deve receber esse número e retornar o dobro dele.

// O programa deve executar as duas etapas e mostrar o resultado final.

// Exercício 6

// Simule o download de um arquivo.

// Regras:

// O download demora 4 segundos.

// Existe 30% de chance de falha.

// Se o download terminar corretamente, retorne:

// "Download concluído"

// Se ocorrer erro, retorne:

// "Erro no download"

// O programa principal deve tratar o sucesso e o erro.

// Exercício 7

// Simule a busca de um usuário em um sistema.

// Regras:

// A busca demora 2 segundos.

// Se o identificador do usuário for 1, o sistema retorna um objeto contendo:

// id: 1

// nome: "Ana"

// Se for qualquer outro identificador, deve ocorrer um erro informando:

// "Usuário não encontrado"

// Exercício 8 (Desafio)

// Simule um carregamento de dados de um servidor.

// Processo:

// O sistema espera 2 segundos.

// Depois gera um número aleatório entre 0 e 1.

// Se o número for maior que 0.7, ocorre um erro com a mensagem:

// "Servidor ocupado"

// Caso contrário, retorna:

// "Dados carregados com sucesso"

// O programa principal deve tratar corretamente sucesso e erro.
